<h3>4.缓存，整站使用文件级缓存，暂使用4处</h3>
<ol>
  <li>1级导航菜单，1小时刷新一次</li>
  <li>2级分类菜单，1小时刷新一次</li>
  <li>首页宣传图片，1小时刷新一次</li>
  <li>首页背景图片集合，1小时刷新一次，此处只缓存了全部图片，每次访问还是会显示不同的图片，缓存的只是总集合。</li>
  <li>强制刷新：删除/protected/runtime/cache/内容，稍候做个按钮实现</li>
</ol>
